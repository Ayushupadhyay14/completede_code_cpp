#include <iostream>

using namespace std;

/*arithmetic operator in c++*/
int main()
{
    int num1;

    int num2;

    cout << "ENTER FAST NUMBER::\n";
    cin >> num1;

    cout << "ENTER SECOND NUMBER::\n";
    cin >> num2;

    cout << "result is num1,\n"

        << num1 + num2 << endl;

    cout << "result is num1,\n"

        << num1 - num2 << endl;

    cout << "result is num1,\n"

        << num1 * num2 << endl;

    cout << "result is num1,\n"
        << num1 / num2 << endl;
    cout << "result is num1,\n"
        << num1 % num2 << endl;

    cout << "\n";
    cout << "HELLO AYUSH UPADHYAY JI\n";

    return 0;
}